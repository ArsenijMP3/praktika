-- 1. ВСЕ КНИГИ (ДЛЯ ГОСТЯ И ЧИТАТЕЛЯ)

SELECT 
    id,
    название,
    автор,
    жанр,
    год_издания,
    издательство,
    количество_экземпляров
FROM Книги
ORDER BY название;

-- 2. ВСЕ ЧИТАТЕЛИ С НАЗВАНИЯМИ РОЛЕЙ

SELECT 
    Читатели.id,
    Читатели.фио,
    Читатели.логин,
    Роли.название_роли AS Роль,
    Читатели.контакт
FROM Читатели 
INNER JOIN Роли ON Читатели.роль_id = Роли.id
ORDER BY Читатели.фио;

-- 3. ВСЕ ВЫДАЧИ С ФИО ЧИТАТЕЛЯ И НАЗВАНИЕМ КНИГИ
-- (ПРОВЕРКА РАБОТЫ ВНЕШНИХ КЛЮЧЕЙ)

SELECT 
    Выдачи.id AS Номер_выдачи,
    Выдачи.дата_выдачи AS Дата_выдачи,
    IIf(Выдачи.дата_возврата IS NULL, 'Не возвращена', Выдачи.дата_возврата) AS Дата_возврата,
    Выдачи.статус AS Статус,
    Читатели.фио AS Читатель,
    Книги.название AS Книга,
    Книги.автор AS Автор
FROM (Выдачи 
INNER JOIN Читатели ON Выдачи.читатель_id = Читатели.id)
INNER JOIN Книги ON Выдачи.книга_id = Книги.id
ORDER BY Выдачи.дата_выдачи DESC;

-- 4. АКТИВНЫЕ ВЫДАЧИ (КНИГИ НА РУКАХ)

SELECT 
    Читатели.фио AS Читатель,
    Книги.название AS Книга,
    Книги.автор AS Автор,
    Выдачи.дата_выдачи AS Дата_выдачи,
    DateDiff('d', Выдачи.дата_выдачи, Date()) AS Дней_на_руках
FROM (Выдачи 
INNER JOIN Читатели ON Выдачи.читатель_id = Читатели.id)
INNER JOIN Книги ON Выдачи.книга_id = Книги.id
WHERE Выдачи.статус = 'активна'
ORDER BY Выдачи.дата_выдачи;

-- 5. ПРОСРОЧЕННЫЕ ВЫДАЧИ (ЕСЛИ БОЛЬШЕ 30 ДНЕЙ)

SELECT 
    Читатели.фио AS Читатель,
    Книги.название AS Книга,
    Выдачи.дата_выдачи AS Дата_выдачи,
    DateDiff('d', Выдачи.дата_выдачи, Date()) AS Дней_задолженности
FROM (Выдачи 
INNER JOIN Читатели ON Выдачи.читатель_id = Читатели.id)
INNER JOIN Книги ON Выдачи.книга_id = Книги.id
WHERE Выдачи.статус = 'активна' 
  AND DateDiff('d', Выдачи.дата_выдачи, Date()) > 30
ORDER BY Дней_задолженности DESC;

-- 6. ФИЛЬТРАЦИЯ КНИГ ПО ЖАНРУ (ДЛЯ БИБЛИОТЕКАРЯ)

SELECT 
    id,
    название,
    автор,
    жанр,
    год_издания,
    издательство,
    количество_экземпляров
FROM Книги
WHERE жанр = 'Роман'
ORDER BY название;

-- 7. ФИЛЬТРАЦИЯ КНИГ ПО ГОДУ ИЗДАНИЯ (ПОСЛЕ 1950)

SELECT 
    id,
    название,
    автор,
    жанр,
    год_издания,
    издательство,
    количество_экземпляров
FROM Книги
WHERE год_издания >= 1950
ORDER BY год_издания DESC;

-- 8. ПОИСК КНИГ ПО НАЗВАНИЮ (СОДЕРЖИТ СЛОВО)

SELECT 
    id,
    название,
    автор,
    жанр,
    год_издания
FROM Книги
WHERE название LIKE '*мир*'
ORDER BY название;


